    private fun replace(provider: Consumer<FinishedRecipe>) {
        remove(provider)
        GTRecipeTypes.ELECTROLYZER_RECIPES.recipeBuilder("decomposition_electrolyzing_cooperite")
            .duration(648)
            .inputItems(ChemicalHelper.get(TagPrefix.dust, GTMaterials.Cooperite))
            .outputItems(
                ChemicalHelper.get(TagPrefix.dust, GTNNMaterials.PlatinumMetal, 3),
                ChemicalHelper.get(TagPrefix.dust, GTMaterials.Nickel),
                ChemicalHelper.get(TagPrefix.dust, GTMaterials.Sulfur),
                ChemicalHelper.get(TagPrefix.dust, GTNNMaterials.PalladiumMetal)
            )
            .EUt(60).save(provider)
        VanillaRecipeHelper.addShapelessRecipe(
            provider,
            "centrifuged_ore_to_dust_platinum",
            ChemicalHelper.get(TagPrefix.dust, GTNNMaterials.PlatinumMetal),
            GTToolType.HARD_HAMMER.itemTags[0],
            UnificationEntry(TagPrefix.crushedRefined, GTMaterials.Platinum)
        )
        VanillaRecipeHelper.addShapelessRecipe(
            provider,
            "centrifuged_ore_to_dust_palladium",
            ChemicalHelper.get(TagPrefix.dust, GTNNMaterials.PalladiumMetal),
            GTToolType.HARD_HAMMER.itemTags[0],
            UnificationEntry(TagPrefix.crushedRefined, GTMaterials.Palladium)
        )
    }

